const lines = text.split('\n');
let category = "อื่นๆ";
let subCategory = "";
const products = [];

for (const raw of lines) {
  const line = raw.trim();
  if (!line) continue;
  
  if (line.startsWith('# ')) {
    category = line.replace('# ', '').trim();
    subCategory = "";
  } else if (line.startsWith('## ')) {
    subCategory = line.replace('## ', '').trim();
  } else if (line.startsWith('### ')) {
    subCategory = line.replace('### ', '').trim();
  } else if (line.startsWith('• ')) {
    // line looks like: • 1 วัน — 7 บาท (+เคลม 2 บาท)
    let prodLine = line.replace('• ', '');
    
    // Parse price
    // Usually formatted like "1 วัน — 7 บาท" or "30 วัน — 1,499 บาท"
    let parts = prodLine.split(' — ');
    if (parts.length === 2) {
      let title = parts[0].trim();
      let pricePart = parts[1].trim();
      
      let priceMatch = pricePart.match(/([\d,]+)\s*บาท/);
      let price = 0;
      let suffix = "";
      
      if (priceMatch) {
        price = parseInt(priceMatch[1].replace(/,/g, ''), 10);
        // keep everything after "บาท" like "(+เคลม 2 บาท)"
        suffix = pricePart.replace(priceMatch[0], '').trim();
      } else {
        // fallback
        let numMatch = pricePart.match(/[\d,]+/);
        if (numMatch) {
          price = parseInt(numMatch[0].replace(/,/g, ''), 10);
        }
      }
      
      let finalName = title;
      if (subCategory) finalName = subCategory + ' ' + finalName;
      if (suffix) finalName += ' ' + suffix;
      
      products.push({
        name: finalName,
        price,
        category,
        stock: 999
      });
    }
  }
}

const fs = require('fs');
fs.writeFileSync('parsed_preemoji.json', JSON.stringify(products, null, 2));
